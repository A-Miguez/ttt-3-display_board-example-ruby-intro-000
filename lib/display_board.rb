# Define a method display_board that prints a 3x3 Tic Tac Toe Board
def display_board
  cell = "   "
  pipe = '|'
  divider = '-----------'
  return cell, pipe, cell, pipe, cell
  return divider
  return cell, pipe, cell, pipe, cell
  return divider
  return cell, pipe, cell, pipe, cell
end
display_board
